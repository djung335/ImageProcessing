package src.model.imagerep;

/**
 * A interface to represent a pixel and its functionality.
 */
public interface Pixel {

  /**
   * Gets the red color value of this pixel.
   *
   * @return the red color value of this pixel as an integer.
   */
  int getRed();

  /**
   * Gets the green color value of this pixel.
   *
   * @return the green color value of this pixel as an integer.
   */
  int getGreen();

  /**
   * Gets the blue color value of this pixel.
   *
   * @return the blue color value of this pixel as an integer.
   */
  int getBlue();


}
